        internal static int Measure(string text, int scale = 1)
        {
            return Math.Max(0, ((text?.Length ?? 0) * 6 * scale) - scale);
        }

        internal static void Draw(Bitmap target, int x, int y, string text, Color color, int scale = 1)
        {
            if (target == null || string.IsNullOrEmpty(text))
            {
                return;
            }

            int cursor = x;
            foreach (char source in text.ToUpperInvariant())
            {
                char character = Glyphs.ContainsKey(source) ? source : '?';
                string[] rows = Glyphs[character];
                for (int row = 0; row < rows.Length; row++)
                {
                    for (int column = 0; column < rows[row].Length; column++)
                    {
                        if (rows[row][column] != '1')
                        {
                            continue;
                        }

                        for (int sy = 0; sy < scale; sy++)
                        {
                            for (int sx = 0; sx < scale; sx++)
                            {
                                int px = cursor + (column * scale) + sx;
                                int py = y + (row * scale) + sy;
                                if (px >= 0 && py >= 0 && px < target.Width && py < target.Height)
                                {
                                    target.SetPixel(px, py, color);
                                }
                            }
                        }
                    }
                }
                cursor += 6 * scale;
            }
        }

        internal static void DrawCentered(Bitmap target, Rectangle bounds, string text, Color color, int scale = 1)
        {
            int x = bounds.X + ((bounds.Width - Measure(text, scale)) / 2);
            int y = bounds.Y + ((bounds.Height - (7 * scale)) / 2);
            Draw(target, x, y, text, color, scale);
        }

